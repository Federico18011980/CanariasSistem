    private void btnAgregarPagoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarPagoActionPerformed
        // TODO add your handling code here:
     
    if (txtMonto.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ingrese un monto");
        return;
    }

    BigDecimal monto;
    try {
        monto = new BigDecimal(txtMonto.getText());
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Monto inválido");
        return;
    }

    if (monto.compareTo(BigDecimal.ZERO) <= 0) {
        JOptionPane.showMessageDialog(this, "El monto debe ser mayor a cero");
        return;
    }

    TipoPago tipo = (TipoPago) cmbTipoPago.getSelectedItem();

    // ❗ VALIDACIÓN CORRECTA: contra saldo
    if (monto.compareTo(saldo) > 0) {
        JOptionPane.showMessageDialog(this, "El monto supera el saldo pendiente");
        return;
    }

    // ✅ CREAR PAGO
    Pago pago = crearPago(tipo, monto);

    // ✅ AGREGAR A LISTA
    pagos.add(pago);

    // ✅ ACUMULAR CORRECTAMENTE
    montoBruto = montoBruto.add(pago.getMontoBruto());
    totalPagado = totalPagado.add(pago.getMontoFinal());

    // ✅ RECALCULAR SALDO (SIEMPRE CON MONTO BRUTO)
    saldo = totalVenta.subtract(montoBruto);

    // ✅ MOSTRAR EN TABLA
    agregarPagoATabla(pago);

    // ✅ ACTUALIZAR LABELS
    actualizarTotales();

    txtMonto.setText("");



    
    }//GEN-LAST:event_btnAgregarPagoActionPerformed

    private void btnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarActionPerformed

     // Verificar si la lista de pagos no está vacía
    if (pagos.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Debe ingresar al menos un pago");
        return;
    }

    // Verificar que el total pagado sea suficiente (o igual) al total de la venta
    if (montoBruto.compareTo(totalVenta) < 0) {
        JOptionPane.showMessageDialog(this, "El monto pagado no cubre el total de la venta");
        return;
    }

    // Verificar que no haya un pago mayor al total de la venta
    if (totalPagado.compareTo(totalVenta) > 0) {
        JOptionPane.showMessageDialog(this, "El monto pagado excede el total de la venta. No se permite el vuelto.");
        return;
    }

    // Si todas las validaciones pasan, cerramos el diálogo
    this.dispose();


    }//GEN-LAST:event_btnFinalizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        
    int fila = tblPagos.getSelectedRow();

    if (fila == -1) {
        JOptionPane.showMessageDialog(this, "Seleccione un pago para eliminar");
        return;
    }

    // Obtener el pago desde la lista
    Pago p = pagos.get(fila);

    // Restar montos
    montoBruto = montoBruto.subtract(p.getMontoBruto());
    totalPagado = totalPagado.subtract(p.getMontoFinal());

    // Eliminar de la lista
    pagos.remove(fila);

    // Eliminar de la tabla
    DefaultTableModel modelo = (DefaultTableModel) tblPagos.getModel();
    modelo.removeRow(fila);

    // Recalcular totales
    actualizarTotales();

    
    }//GEN-LAST:event_btnEliminarActionPerformed

