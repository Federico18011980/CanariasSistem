    private void cargarTiposPago() {
        cmbTipoPago.removeAllItems();
        cmbTipoPago.addItem(TipoPago.EFECTIVO);
        cmbTipoPago.addItem(TipoPago.TRANSFERENCIA);
        cmbTipoPago.addItem(TipoPago.CREDITO);
    }


private void actualizarTotales() {

    if (totalVenta == null) totalVenta = BigDecimal.ZERO;
    if (totalPagado == null) totalPagado = BigDecimal.ZERO;
    if (montoBruto == null) montoBruto = BigDecimal.ZERO;

    saldo = totalVenta.subtract(montoBruto);

    lblTotalVenta.setText("Total venta: $" + totalVenta);
    lblTotalPagado.setText("Total pagado: $" + montoBruto);
    lblTotalCancelado.setText("Total cobrado: $" + totalPagado);
    lblSaldo.setText("Saldo: $" + saldo);
}



    private Pago crearPago(TipoPago tipo, BigDecimal montoBruto) {

        BigDecimal porcentaje = obtenerPorcentajeDescuento(tipo);
        BigDecimal descuento = montoBruto
                .multiply(porcentaje)
                .divide(new BigDecimal("100"));

        BigDecimal montoFinal = montoBruto.subtract(descuento);

        Pago p = new Pago();
        p.setTipo(tipo);
        p.setMontoBruto(montoBruto);
        p.setDescuento(descuento);
        p.setMontoFinal(montoFinal);

        return p;
    }
    
    private BigDecimal obtenerPorcentajeDescuento(TipoPago tipo) {

        Config conf = confDao.obtenerConfig();

        switch (tipo) {
            case EFECTIVO:
                return conf.getEfectivo();
            case TRANSFERENCIA:
                return conf.getTransferencia();
            case CREDITO:
                return conf.getCredito();
            default:
                return BigDecimal.ZERO;
        }
    }
   
    private void agregarPagoATabla(Pago p) {

        DefaultTableModel modelo = (DefaultTableModel) tblPagos.getModel();

        modelo.addRow(new Object[]{
            p.getTipo(),
            p.getMontoBruto(),
            p.getDescuento(),
            p.getMontoFinal()
        });
    }
  

